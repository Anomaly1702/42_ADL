#include "libft.h"

void	ft_bzero(void *string, int n)
{
	char	*str;
	int		i;

	str = (char *)string;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		str[i] = '\0';
		i ++;
	}
}
